# 整数
print('3 + 4 =', 3 + 4)
print('10 - 7 =', 10 - 7)
print('8 / 2 =', 8 / 2)
print('5 x 6 =', 5 * 6)
print('(3 + 4) x (4 + 5) =', (3 + 4) * (4 + 5))

# 浮点数
print('3.1 + 4.6 =', 3.1 + 4.6)
print('3.1 + 4.6 =', round(3.1 + 4.6, 1))
print('10.4 - 7.8 =', round(10.4 - 7.8, 1))
print('8.4 / 2.5 =', round(8.4 / 2.5, 1))
print('5.2 x 6.3 =', 5.2 * 6.3)
print('(3.3 - 4.2) x (4.5 + 5.3) =',
      round((3.3 - 4.2) * (4.5 + 5.3), 2))

# 取整数
print('7 ÷ 3 =', 7 // 3, '（取整）')
# 取余数
print('7 ÷ 3 =', 7 // 3, '余', 7 % 3)
# 幂运算
print('2 x 2 x 2 =', 2 ** 3)

# 判断大小
print('4 = 4:', 4 == 4)
print('3 < 4:', 3 < 4)
print('5 >= 3:', 5 >= 3)
print('5 <= 3:', 5 <= 3)
print('5 != 3:', 5 != 3)
